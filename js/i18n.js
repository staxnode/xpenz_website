document.addEventListener('DOMContentLoaded', () => {
    const langBtn = document.getElementById('lang-toggle');
    const currentLang = localStorage.getItem('app_lang') || 'en';
    
    // Set initial language
    setLanguage(currentLang);

    if (langBtn) {
        langBtn.addEventListener('click', () => {
            const newLang = localStorage.getItem('app_lang') === 'en' ? 'bn' : 'en';
            setLanguage(newLang);
        });
    }
});

function setLanguage(lang) {
    localStorage.setItem('app_lang', lang);
    const langBtnText = document.getElementById('lang-toggle-text');
    if (langBtnText) {
        langBtnText.textContent = lang === 'en' ? 'EN' : 'BN';
    }

    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key]; // Using innerHTML to preserve spans like text-gradient
        }
    });
    
    // Update body class for font switching (optional, if using different fonts for bangla)
    if (lang === 'bn') {
        document.body.classList.add('lang-bn');
    } else {
        document.body.classList.remove('lang-bn');
    }
}
